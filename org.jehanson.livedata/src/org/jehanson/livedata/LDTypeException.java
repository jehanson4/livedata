package org.jehanson.livedata;

/**
 * 
 * @author jehanson
 */
public class LDTypeException extends Exception {

	private static final long serialVersionUID = 8029599387726086390L;

	public LDTypeException() {
		super();
	}

	public LDTypeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public LDTypeException(String arg0) {
		super(arg0);
	}

	public LDTypeException(Throwable arg0) {
		super(arg0);
	}

}
